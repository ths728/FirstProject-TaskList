package model;

public class Chess {
    private int x1;
    private int y1;

    public Chess(int x, int y) {
        this.x1 = x;
        this.y1 = y;
    }

    public int getX() {
        return x1;
    }

    public int getY() {
        return y1;
    }

    public void setX() {
        this.x1 = x1;
    }

    public void setY() {
        this.y1 = y1;
    }
}
